    private void graficar(int totalHombres, int totalMujeres){
        //aqui va el metodo para setear el porcentaje de mujeres y el porcentaje de hombres
        
        //optenemos el total de personas para sacar la estadistica
        float TotalPersonas = totalHombres + totalMujeres;
        
        float pocentajeHombres = ((totalHombres*100)/TotalPersonas);
        float porcentajeMujeres = ((totalMujeres*100)/TotalPersonas);
        
        
        //seteamos los label
        labelHombresPorc.setText(String.format("%.2f", pocentajeHombres)+"%");        
        labelMujeresPorc.setText(String.format("%.2f", porcentajeMujeres)+"%");
        
        //seteamos los progressbar
        jProgressBarHombres.setValue((int)pocentajeHombres);        
        jProgressBarMujeres.setValue((int)porcentajeMujeres);
       
    }
    
     
    
    public static Double formatearDecimales(Double numero, Integer numeroDecimales) {
       return Math.round(numero * Math.pow(10, numeroDecimales)) / Math.pow(10, numeroDecimales);
    }

